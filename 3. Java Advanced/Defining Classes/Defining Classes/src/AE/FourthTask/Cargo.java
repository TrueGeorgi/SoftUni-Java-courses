package AE.FourthTask;

public class Cargo {

    int weight;

    String type;

    // constructor


    public Cargo(int weight, String type) {
        this.weight = weight;
        this.type = type;
    }

    // getters


    public int getWeight() {
        return weight;
    }

    public String getType() {
        return type;
    }
}
