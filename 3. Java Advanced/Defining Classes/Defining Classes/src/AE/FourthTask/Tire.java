package AE.FourthTask;

public class Tire {

    double pressure;

    int age;

                                  // constructor


    public Tire(double pressure, int age) {
        this.pressure = pressure;
        this.age = age;
    }

                                  // getters

    public double getPressure() {
        return pressure;
    }

    public int getAge() {
        return age;
    }
}
