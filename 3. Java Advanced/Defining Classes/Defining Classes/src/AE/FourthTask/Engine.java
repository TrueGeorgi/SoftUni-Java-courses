package AE.FourthTask;

public class Engine {

    int speed;

    int power;

    // constructors

    public Engine(int speed, int power) {
        this.speed = speed;
        this.power = power;
    }


    // getters

    public int getSpeed() {
        return speed;
    }

    public int getPower() {
        return power;
    }
}
